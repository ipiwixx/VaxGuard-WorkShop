<h2>Conformité RGPD et autres lois de protection des données</h2>
<br>
    <p>Pour garantir la conformité aux lois comme le RGPD (Règlement Général sur la Protection des Données), VAXGUARD met en place les mesures suivantes :</p>
    <br>
    <ul>
        <li><strong>Consentement explicite</strong> : Lors de l'inscription, les utilisateurs doivent accepter explicitement la collecte et l'utilisation de leurs données. Ils peuvent aussi donner leur accord pour recevoir des rappels et des notifications.</li>
        <li><strong>Accès et rectification</strong> : Chaque utilisateur (pédiatre ou parent) a le droit de consulter, modifier ou supprimer ses données personnelles à tout moment, via son profil.</li>
        <li><strong>Portabilité des données</strong> : En conformité avec le RGPD, les utilisateurs peuvent demander une copie de leurs données pour les transférer vers un autre service si nécessaire.</li>
        <li><strong>Droit à l'oubli</strong> : Si un utilisateur souhaite supprimer son compte, ses données seront effacées de manière sécurisée après une période définie (ex: 3 à 6 mois).</li>
        <li><strong>Sécurité des données</strong> : Toutes les informations sont stockées de manière sécurisée à l’aide de techniques de chiffrement (cryptage des mots de passe et des données sensibles). Les accès sont limités aux seules personnes autorisées.</li>
        <li><strong>Limitation de la collecte</strong> : Nous ne collectons que les informations strictement nécessaires pour le fonctionnement de l’application et ne partageons aucune donnée avec des tiers sans consentement préalable.</li>
    </ul>

